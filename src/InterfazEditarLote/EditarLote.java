    private void botonborrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonborrarActionPerformed

        
        int indice, identi,valordearray;
        indice=tabla.getSelectedRow();
        identi=Integer.parseInt((String)dtm.getValueAt(indice,0));

        Campo buscadocampo = new Campo();
        
        buscadocampo = Controlador.ObtenerCampo(Long.valueOf(ide));
        for(Lote lotebuscado: buscadocampo.getLotes()){
                    if(lotebuscado.getIdLote()==identi){
                        int getIndex=buscadocampo.getLotes().indexOf(lotebuscado);
                          buscadocampo.getLotes().remove(getIndex);
                          Controlador.BorrarLote(lotebuscado);
                          break;
                    }
        }

    }//GEN-LAST:event_botonborrarActionPerformed

