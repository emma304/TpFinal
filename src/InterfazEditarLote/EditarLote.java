    public EditarLote(controlador control, int numerocampo) {
         ide = numerocampo;
         Controlador = control;
         initComponents();

         
         List<Campo> campos = new ArrayList();
         campos = Controlador.ObtenerTodosLosCampos();
         for(Campo ca: campos){
             if(ca.getNroCampo()==ide){
                
                 
                List <Lote> lista = new ArrayList();
                String [] titulo = new String []  {"Número de lote","Hectareas","Tipo de suelo","Nombre Tipo de Suelo"};
                dtm.setColumnIdentifiers(titulo);
                tabla.setModel(dtm);
                lista=Controlador.ObtenerTodosLosLotes();
                lista.stream().map((obj) -> new String []{Long.toString(obj.getIdLote()),Long.toString(obj.getSuperficie()),Long.toString(obj.getTiposuelo().getIdTipoSuelo()),obj.getTiposuelo().getDescripcion()}).forEachOrdered((lotes) -> {
                dtm.addRow(lotes);});
                 
             }
         }
         if(this.tabla.getSelectedRow()==-1){
            this.botonborrar.setEnabled(false);
            this.botoneditar.setEnabled(false);
            this.botondividir.setEnabled(false);
         }
    }
    
